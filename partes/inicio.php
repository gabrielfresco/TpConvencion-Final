			<p>“Lo que trata de hacer la convención,” dijo el embajador neozelandés Don MacKay, Presidente del Comité Especial que negoció el texto, “es elaborar detalladamente los derechos de las personas con discapacidad y establecer un código de aplicación”.

A continuación sigue un resumen de las provisiones más importantes de la convención.<br>

Los países que se unen a la convención se comprometen a elaborar y poner en práctica políticas, leyes y medidas administrativas para asegurar los derechos reconocidos en la convención y abolir las leyes, reglamentos, costumbres y prácticas que constituyen discriminación (Artículo 4).<br>


Habida cuenta de que es esencial que cambien las percepciones para mejorar la situación de las personas con discapacidad, los países que ratifican la convención deben combatir los estereotipos y prejuicios y promover la conciencia de las capacidades de esas personas (Artículo 8).<br>


Los países deben garantizar que las personas con discapacidad disfruten del derecho inherente a la vida en un pie de igualdad con otras personas (Artículo 10), asegurar la igualdad de derechos y el adelanto de las mujeres y las niñas con discapacidad (Artículo 6) y proteger a los niños con discapacidad (Artículo 7).<br>


Los niños con discapacidad tendrán igualdad de derechos, no serán separados de sus padres contra su voluntad, excepto cuando las autoridades determinen que ello es en el interés superior del niño, y en ningún caso serán separados de sus padres debido a una discapacidad del niño o de los padres (Artículo 23).</p>			

